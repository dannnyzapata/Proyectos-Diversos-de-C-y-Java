package genericos;

/**
 *
 * @author Mac18
 */
public class Vehiculo {
    public double x;
    public double y;
    public double z;
    
    public Vehiculo(){
        
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }
    
}
